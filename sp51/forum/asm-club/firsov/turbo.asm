.model tiny,c
.code
org 100h
start:
	mov dx,43h
	mov al,074h	;упр. слово 2-й канал: запись/чтение 
	out dx,al	;младшего, затем старшего байта
	mov dx,41h	;2-й канал
	mov al,020h	;младший байт
	out dx,al	
	mov al,00h	;старший
	out dx,al
	int 20h
end start