;В случае работы процессора с памятью, может дать ощутимый эффект
;( см. ПрефКлуб - думает быстрей ).
;Для каждой машины необходима своя настройка: 
;	начиная с младшего байта( регистра задвижки 2-го канала таймера),
; 	постепенно увеличивать его значение, параллельно контролируя
;	параметры системы( напр. скорость передачи данных контроллером
;	дисков ). Увеличение имеет смысл, пока оные не начнут ухудшатся.
;		Контроль удобно проводить SistemInfo Нортона:Menu-Benchmark.
;	После достижения критических характеристик( или ошибки памяти -
	подвисания машны) полученное значение следует уменьшить на 20 - 50% .
;		Редактировать имеет смысл прямо com-файл двоичным редактором.
;	Не перепутайте байты, иначе придется перегружаться.
